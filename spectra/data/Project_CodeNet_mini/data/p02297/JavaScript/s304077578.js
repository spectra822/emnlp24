function main(lines) {
    lines = lines.map(function(line) {
        return line.split(" ").filter(function(e) {
            return e != "";
        }).map(function(e) {
            var n = Number(e);
            if (n.toString() == "NaN") {
                return e.replace(/(\r)|(\n)/g, "");
            }
            return n;
        });
    });
    var last = lines.pop();
    if (last.length == 2) {
        lines.push(last);
    }
    lines.shift(); //????????????n???????????????
    var points = lines;
    //points.reverse();


    var p0 = points[0];
    var ps, pe;
    var S = 0;
    var length = points.length;
    for (var i = 1; i < length - 1; i++) {
        //i = 1,2,3,,,,length-2
        ps = points[i];
        pe = points[i + 1];
        S += sign_s(p0, ps, pe);
    }
    //??????????????????????????????2??§??????
    var r_S = "" + (S / 2);
    var idx = r_S.indexOf(".");
    if (idx == -1) {
        //??´??°
        r_S += ".0";
    } else {
        //?????????
        //????????¨?????§????????????
        r_S = r_S.slice(0, idx + 2);
    }
    log(r_S);
}

function sign_s(p1, p2, p3) {
    //??????????§????
    var pa = [p2[0] - p1[0], p2[1] - p1[1]];
    var pb = [p3[0] - p1[0], p3[1] - p1[1]];
    //a->b??§????¨???????
    return sign_s_main(pa, pb);
}

function sign_s_main(pa, pb) {
    //?????????2??§????????????
    return (pa[0] * pb[1] - pa[1] * pb[0]);
}